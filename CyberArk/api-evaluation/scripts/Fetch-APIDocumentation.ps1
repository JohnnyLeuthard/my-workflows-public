<#
.SYNOPSIS
    Pull live CyberArk PAM API documentation for a version and populate
    versions/v<Version>/endpoints/ with one markdown file per category.

.DESCRIPTION
    Normalizes the version to major.minor for the docs URL, fetches the
    webservices landing page, enumerates linked endpoint category pages,
    and writes one file per category using ../references/endpoint-template.md.

    Does not overwrite existing populated files without -Force.

    NOTE: This is a skeleton. HTML parsing of the CyberArk docs site is
    intentionally left minimal — the function Get-EndpointCategoryLinks and the
    per-endpoint extraction logic should be extended as needed. A -WhatIf run
    will report the URL it would fetch and the folder it would write to
    without making any network calls or file writes.

.PARAMETER Version
    Version string. Accepts 14.2.1, v14.2.1, 14.2, or v14.2. Must match an existing
    versions/v<Version>/ folder (run New-APIVersion.ps1 first if it does not exist).

.PARAMETER Force
    Overwrite existing endpoint files.

.EXAMPLE
    ./Fetch-APIDocumentation.ps1 -Version 14.2.1

.EXAMPLE
    ./Fetch-APIDocumentation.ps1 -Version 14.2.1 -WhatIf
#>
[CmdletBinding(SupportsShouldProcess, ConfirmImpact = 'Medium')]
param(
    [Parameter(Mandatory, Position = 0)]
    [ValidatePattern('^v?\d+\.\d+(\.\d+)?$')]
    [string]$Version,

    [switch]$Force
)

Set-StrictMode -Version Latest
$ErrorActionPreference = 'Stop'

$WorkspaceRoot = Split-Path -Parent $PSScriptRoot

function ConvertTo-VersionParts {
    param([string]$Raw)
    $clean = $Raw.TrimStart('v', 'V')
    $parts = $clean.Split('.')
    [PSCustomObject]@{
        Full        = $clean
        FolderName  = "v$clean"
        MajorMinor  = "$($parts[0]).$($parts[1])"
    }
}

function Get-DocsUrl {
    param([string]$MajorMinor)
    "https://docs.cyberark.com/pam-self-hosted/$MajorMinor/en/content/webservices/implementing%20privileged%20account%20security%20web%20services.htm"
}

function Get-EndpointCategoryLinks {
    <#
    Fetches the webservices landing page for the given major.minor and returns
    an array of @{ Name = 'Accounts'; Url = 'https://.../accounts...htm' }.

    Extraction is intentionally minimal — extend the regex / HTML walk as the
    shape of the CyberArk docs landing page evolves.
    #>
    param([string]$LandingUrl)

    $html = (Invoke-WebRequest -Uri $LandingUrl -UseBasicParsing).Content

    $linkPattern = '(?is)<a\s+[^>]*href="([^"#?]+\.htm)"[^>]*>([^<]+)</a>'
    $linkMatches = [regex]::Matches($html, $linkPattern)

    $base = [Uri]$LandingUrl
    $results = foreach ($m in $linkMatches) {
        $href = $m.Groups[1].Value
        $text = ($m.Groups[2].Value -replace '\s+', ' ').Trim()
        if (-not $text) { continue }
        $absolute = if ($href -match '^https?://') { $href } else { (New-Object Uri($base, $href)).AbsoluteUri }
        [PSCustomObject]@{ Name = $text; Url = $absolute }
    }

    # De-duplicate by Url, preserve order
    $results | Group-Object Url | ForEach-Object { $_.Group[0] }
}

$V = ConvertTo-VersionParts -Raw $Version
$VersionsRoot = Join-Path $WorkspaceRoot 'versions'
$VersionFolder = Join-Path $VersionsRoot $V.FolderName
$EndpointsFolder = Join-Path $VersionFolder 'endpoints'
$LandingUrl = Get-DocsUrl -MajorMinor $V.MajorMinor

if (-not (Test-Path $VersionFolder)) {
    throw "Version folder not found: $VersionFolder. Run New-APIVersion.ps1 -Version $($V.Full) first."
}
if (-not (Test-Path $EndpointsFolder)) {
    if ($PSCmdlet.ShouldProcess($EndpointsFolder, 'Create directory')) {
        New-Item -ItemType Directory -Path $EndpointsFolder -Force | Out-Null
    }
}

Write-Host "Docs URL: $LandingUrl" -ForegroundColor Cyan
Write-Host "Target folder: $EndpointsFolder" -ForegroundColor Cyan

if (-not $PSCmdlet.ShouldProcess($LandingUrl, 'Fetch + parse landing page')) {
    Write-Host "[WhatIf] Would fetch landing page and enumerate endpoint categories." -ForegroundColor Yellow
    return
}

$categories = Get-EndpointCategoryLinks -LandingUrl $LandingUrl
Write-Host "Discovered $($categories.Count) linked pages (pre-filter)." -ForegroundColor Cyan

$templatePath = Join-Path $WorkspaceRoot 'references/endpoint-template.md'
if (-not (Test-Path $templatePath)) {
    throw "Missing template: $templatePath"
}

$written = 0
$skipped = 0

foreach ($cat in $categories) {
    $safeName = ($cat.Name -replace '[^\w\-]', '')
    if (-not $safeName) { continue }

    $outPath = Join-Path $EndpointsFolder "$safeName.md"

    if ((Test-Path $outPath) -and -not $Force) {
        $skipped++
        continue
    }

    $stub = @"
---
filename: $safeName.md
version: $($V.Full)
source_url: $($cat.Url)
build: unknown
status: Framework only
---

# $($cat.Name)

_Stub generated by Fetch-APIDocumentation.ps1. Populate by walking the source_url
page and filling in the per-endpoint sections per ../../../references/endpoint-template.md._

## Overview

| Method | Endpoint | Description | Auth Required |
|---|---|---|---|
| _(unpopulated)_ | | | |
"@

    if ($PSCmdlet.ShouldProcess($outPath, 'Write endpoint stub')) {
        Set-Content -Path $outPath -Value $stub -Encoding UTF8
        $written++
    }
}

# Stamp version-tracking.md with the fetch date
$today = (Get-Date).ToString('yyyy-MM-dd')
$VersionTracking = Join-Path $VersionsRoot 'version-tracking.md'
if (Test-Path $VersionTracking) {
    $content = Get-Content -Path $VersionTracking -Raw
    $rowRegex = "^\| $([regex]::Escape($V.Full)) \|.*$"
    $newRow = "| $($V.Full) | ``$($V.FolderName)/`` | unknown | ``$($V.MajorMinor)`` | Populated | $today | Fetched by Fetch-APIDocumentation.ps1 |"
    if ($content -match $rowRegex) {
        if ($PSCmdlet.ShouldProcess($VersionTracking, "Update row for v$($V.Full)")) {
            ($content -replace $rowRegex, $newRow) | Set-Content -Path $VersionTracking -Encoding UTF8
        }
    }
}

Write-Host "Wrote $written new file(s); skipped $skipped existing (use -Force to overwrite)." -ForegroundColor Cyan
